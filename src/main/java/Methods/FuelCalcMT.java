package Methods;

public class FuelCalcMT {
    public float avaragexrange(float avarage, float range){
        float result1 = range / avarage;
        return result1;
    }

    public float result1xfuel(float fuel, float result1){
        float result2 = fuel * result1;
        return result2;
    }

    public float result2xtime(float time, float result2){
        float result3 = time * result2;
        return result3;
    }
}
